CREATE SEQUENCE SEQ_SEMAFOROS
  START WITH 1
  INCREMENT BY 1
  NOCACHE
  NOCYCLE;

CREATE TABLE TBL_SEMAFOROS (
    ID INTEGER DEFAULT SEQ_SEMAFOROS.NEXTVAL NOT NULL,
    LOCALIZACAO VARCHAR2(40) NOT NULL,
    STATUS VARCHAR2(20) NOT NULL,
    ULTIMO_AJUSTE TIMESTAMP,
    DURACAO_VERDE INTEGER NOT NULL,
    DURACAO_AMARELO INTEGER NOT NULL,
    DURACAO_VERMELHO INTEGER NOT NULL
);
