CREATE SEQUENCE SEQ_PADRAO_TRAFEGO
  START WITH 1
  INCREMENT BY 1
  NOCACHE
  NOCYCLE;

CREATE TABLE TBL_PADRAO_TRAFEGO (
    ID INTEGER DEFAULT SEQ_PADRAO_TRAFEGO.NEXTVAL NOT NULL,
    HORARIO_ANALISE TIMESTAMP NOT NULL,
    FLUXO_MEDIO_VEICULOS INTEGER NOT NULL,
    HORARIOS_PICO VARCHAR2(100),
    AREAS_CONGESTIONADAS VARCHAR2(100)
);
